const form = document.querySelector('.form-content');
const para = document.getElementById('input');
const article = document.querySelector('.article-section');

form.addEventListener('submit', (e) => {
    e.preventDefault();
    const value = parseInt(para.value);
    const random = Math.floor(Math.random()*loremText.length);

    if(isNaN(value) || value <= 0 || value > 9) {
        article.innerHTML = alert ("Sorry! for the inconvenience, it can only generate paragraphs 1 to 9");
    }
    else {
        let Text = loremText.slice(0, value);
        Text = Text.map(function(item) {
            return `<p class="result">${item}</p>`;
        }).join("");

        article.innerHTML = Text;
    }
});
